/*
 * injector.js — in-browser WRAPPER-strategy injector. Mirrors the Java
 * reference engine (pluginpulse-inject) but does all class rewriting through the
 * constant-pool editor, so it needs no bytecode library and is class-version
 * agnostic. WRAPPER only: final main classes are refused with a pointer to the CLI.
 *
 * injectJar(jarBytes, opts, assets) -> Promise<Uint8Array>
 *   opts   : { modrinth, github, hangar, permission, commandRoot, mode, contact,
 *              track, checkIntervalHours, upgrade }
 *   assets : { coreJar: Uint8Array, wrapperTemplate: Uint8Array }
 *
 * Requires JSZip (global) and PPClass (constant-pool.js).
 */
(function (root) {
  'use strict';

  const CP = root.PPClass || (typeof require !== 'undefined' ? require('./constant-pool.js') : null);
  const CORE_INTERNAL = 'io/github/darkstarworks/pluginpulse';
  const CORE_DOTTED = 'io.github.darkstarworks.pluginpulse';
  const MAIN_PLACEHOLDER = 'PP__MAIN__PLACEHOLDER';
  const PULSE_PLACEHOLDER = 'PP__PULSE__PLACEHOLDER';

  function getJSZip() {
    if (root.JSZip) return root.JSZip;
    if (typeof require !== 'undefined') return require('jszip');
    throw new Error('JSZip not available');
  }

  const dec = new TextDecoder('utf-8');
  const enc = new TextEncoder();

  /** Find the descriptor (paper-plugin.yml preferred) and its main class. */
  async function readDescriptor(zip) {
    for (const entry of ['paper-plugin.yml', 'plugin.yml']) {
      const file = zip.file(entry);
      if (!file) continue;
      const text = dec.decode(await file.async('uint8array'));
      const m = text.match(/^\s*main:\s*(.+?)\s*$/m);
      if (m) {
        let main = m[1].trim().replace(/^['"]|['"]$/g, '');
        return { entry, text, main };
      }
    }
    return null;
  }

  function hasPluginPulse(zip) {
    return Object.keys(zip.files).some((p) =>
      p.endsWith('/pluginpulse/PluginPulse.class') || p === CORE_INTERNAL + '/PluginPulse.class');
  }

  function buildPulseYaml(o) {
    const lines = ['# Generated by the PluginPulse web tool.'];
    if (o.modrinth) lines.push('modrinth: ' + o.modrinth);
    if (o.github) lines.push('github: ' + o.github);
    if (o.hangar) lines.push('hangar: ' + o.hangar);
    // Explicit check order (primary first). Only meaningful with 2+ sources.
    if (o.sourceOrder && o.sourceOrder.length > 1) {
      lines.push('source-order: [' + o.sourceOrder.join(', ') + ']');
    }
    if (o.permission) lines.push('permission: ' + o.permission);
    if (o.commandRoot) lines.push('command-root: ' + o.commandRoot);
    if (o.contact) lines.push('user-agent-contact: "' + o.contact + '"');
    lines.push('mode: ' + (o.mode || 'notify'));
    if (o.checkIntervalHours) lines.push('check-interval-hours: ' + o.checkIntervalHours);
    if (o.track) lines.push('track: ' + o.track);
    return lines.join('\n') + '\n';
  }

  function isSignatureEntry(name) {
    const u = name.toUpperCase();
    return u.startsWith('META-INF/') &&
      (u.endsWith('.SF') || u.endsWith('.RSA') || u.endsWith('.EC') || u.endsWith('.DSA'));
  }

  /** Pull human-readable metadata out of a plugin.yml / paper-plugin.yml body. */
  function parseMeta(text) {
    const scalar = (key) => {
      const m = text.match(new RegExp('^\\s*' + key + ':\\s*(.+?)\\s*$', 'm'));
      return m ? m[1].trim().replace(/^['"]|['"]$/g, '') : null;
    };
    const authors = [];
    const single = scalar('author');
    if (single) authors.push(single);
    const inline = text.match(/^\s*authors:\s*\[(.+?)\]\s*$/m);
    if (inline) {
      inline[1].split(',').forEach((a) => {
        const v = a.trim().replace(/^['"]|['"]$/g, '');
        if (v) authors.push(v);
      });
    } else {
      const block = text.match(/^\s*authors:\s*\n((?:[ \t]*-[ \t]*.+\n?)+)/m);
      if (block) {
        block[1].split('\n').forEach((line) => {
          const mm = line.match(/^[ \t]*-[ \t]*(.+?)\s*$/);
          if (mm) authors.push(mm[1].replace(/^['"]|['"]$/g, ''));
        });
      }
    }
    return {
      name: scalar('name'),
      version: scalar('version'),
      authors: [...new Set(authors.filter(Boolean))],
    };
  }

  /** Inspect without modifying — powers the "Preview jar" button. */
  async function inspectJar(jarBytes) {
    const JSZip = getJSZip();
    const zip = await JSZip.loadAsync(jarBytes);
    const desc = await readDescriptor(zip);
    if (!desc) throw new Error('No plugin.yml or paper-plugin.yml with a main: entry.');
    let finalMain = false;
    const mainEntry = zip.file(desc.main.replace(/\./g, '/') + '.class');
    if (mainEntry) finalMain = CP.isFinal(await mainEntry.async('uint8array'));
    const meta = parseMeta(desc.text);
    return {
      descriptor: desc.entry,
      main: desc.main,
      name: meta.name,
      version: meta.version,
      authors: meta.authors,
      finalMain,
      alreadyInjected: hasPluginPulse(zip),
      strategy: finalMain ? 'INSTRUMENT (unsupported in-browser)' : 'WRAPPER',
    };
  }

  async function injectJar(jarBytes, opts, assets) {
    const JSZip = getJSZip();
    const zip = await JSZip.loadAsync(jarBytes);

    const desc = await readDescriptor(zip);
    if (!desc) throw new Error('No plugin.yml or paper-plugin.yml with a main: entry.');
    if (!opts.modrinth && !opts.github && !opts.hangar) {
      throw new Error('Provide at least one update source (Modrinth, GitHub, or Hangar).');
    }
    if (hasPluginPulse(zip) && !opts.upgrade) {
      throw new Error('This jar already contains PluginPulse. Enable "re-inject" to proceed.');
    }

    const mainEntry = zip.file(desc.main.replace(/\./g, '/') + '.class');
    if (mainEntry) {
      const mainBytes = await mainEntry.async('uint8array');
      if (CP.isFinal(mainBytes)) {
        throw new Error('The main class ' + desc.main + ' is final and cannot be subclassed. '
          + 'Use the command-line tool (pluginpulse-inject), which instruments final mains in place.');
      }
    }

    const mainInternal = desc.main.replace(/\./g, '/');
    const slash = mainInternal.lastIndexOf('/');
    const pkgInternal = slash >= 0 ? mainInternal.slice(0, slash) : '';
    const relocInternal = pkgInternal ? pkgInternal + '/pluginpulse' : 'pluginpulse';
    const relocDotted = relocInternal.replace(/\//g, '.');

    // 1. Relocate the core into the target's package.
    const coreZip = await JSZip.loadAsync(assets.coreJar);
    const corePaths = Object.keys(coreZip.files);
    for (const path of corePaths) {
      const f = coreZip.files[path];
      if (f.dir) continue;
      if (path.startsWith('META-INF/') || path === 'module-info.class') continue;
      let data = await f.async('uint8array');
      let newPath = path;
      if (path.startsWith(CORE_INTERNAL + '/')) {
        newPath = relocInternal + '/' + path.slice(CORE_INTERNAL.length + 1);
      }
      if (path.endsWith('.class')) {
        data = CP.editClassUtf8(data, [
          [CORE_INTERNAL, relocInternal],
          [CORE_DOTTED, relocDotted],
        ]);
      }
      zip.file(newPath, data);
    }

    // 2. Instantiate the wrapper from the template.
    const wrapperBytes = CP.editClassUtf8(assets.wrapperTemplate, [
      [MAIN_PLACEHOLDER, mainInternal],
      [PULSE_PLACEHOLDER, relocInternal],
    ]);
    zip.file(mainInternal + '__Pulse.class', wrapperBytes);

    // 3. Re-point main: to the wrapper (text edit preserves comments/formatting).
    const newText = desc.text.replace(/^(\s*main:\s*).*$/m, (_, p1) => p1 + desc.main + '__Pulse');
    zip.file(desc.entry, enc.encode(newText));

    // 4. Add pluginpulse.yml.
    zip.file('pluginpulse.yml', enc.encode(buildPulseYaml(opts)));

    // 5. Drop any signature (adding classes voids it anyway).
    for (const path of Object.keys(zip.files)) {
      if (isSignatureEntry(path)) zip.remove(path);
    }

    return zip.generateAsync({ type: 'uint8array', compression: 'DEFLATE' });
  }

  const api = { injectJar, inspectJar, readDescriptor, buildPulseYaml };
  if (typeof module !== 'undefined' && module.exports) module.exports = api;
  root.PPInjector = api;
})(typeof window !== 'undefined' ? window : globalThis);
